function result = subtractNumbers(a, b)
    % Subtract two numbers with MATLAB
    %
    % Args:
    %   a (double): Input value 1
    %   b (double): Input value 2
    %
    % Returns:
    %   double: The subtraction of the input values
    %
    % Examples:
    %   >>> subtractNumbers(2,2)
    result = a - b;
    end
